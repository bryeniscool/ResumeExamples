using System;
using System.IO;
using TagLib;
/////////////////////////////////////////////////////////
///End Library Imports///

///NAMESPACE BandcampMusicFileRename.DataGrab///
/////////////////////////////////////////////////////////
namespace BandcampMusicFileRename.DataGrab
{

///CLASS GetYear///
/////////////////////////////////////////////////////////
    public static class GetYear
    {

///METHOD GetYearDiscovery///
/////////////////////////////////////////////////////////
        public static UInt32 GetYearDiscovery(string musicFilePath)
        {
            TagLib.File file = TagLib.File.Create(musicFilePath);
            return file.Tag.Year;
        }
/////////////////////////////////////////////////////////
///End METHOD GetYearDiscovery///
    }
/////////////////////////////////////////////////////////
///End CLASS GetYear///
}
